    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        switch (evt.getKeyCode()) {
            case 17:
                this.crearRuta = true;
                break;
            case 82:
                EliminarRuta dialog = new EliminarRuta(new javax.swing.JFrame(), true);
                dialog.definirParametro(this.nebulosaControl.getNebulosa());
                dialog.setVisible(true);
                new ControladorDeCarga().guardarCambiosArchivos();
                break;
            default:
                System.out.println("key evt:" + evt.getKeyCode());
        }
        
    }//GEN-LAST:event_formKeyPressed

    private void formKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyReleased
        
        switch (evt.getKeyCode()) {
            case 17:
                this.crearRuta = false;
                break;
            default:
                System.out.println("key evt:" + evt.getKeyCode());
        }
        
    }//GEN-LAST:event_formKeyReleased

    private void panelNebulosa2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelNebulosa2MousePressed
        if ((evt.getButton() == 1)) {
            if (crearRuta) {
                this.nebulosaControl.lineaTransicion[0] = evt.getX();
                this.nebulosaControl.lineaTransicion[1] = evt.getY();
                this.nebulosaControl.lineaTransicion[2] = evt.getX();
                this.nebulosaControl.lineaTransicion[3] = evt.getY();
                this.panelNebulosa2.setLineaTransicion(this.nebulosaControl.lineaTransicion);
            }
        }
    }//GEN-LAST:event_panelNebulosa2MousePressed

    private void panelNebulosa2MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelNebulosa2MouseReleased
        if (evt.getButton() == 1) {
            if (crearRuta) {
                crearRuta = !crearRuta;
                this.nebulosaControl.evaluarTransicion();
                this.panelNebulosa2.setLineaTransicion(this.nebulosaControl.lineaTransicion);
            }
        }
    }//GEN-LAST:event_panelNebulosa2MouseReleased

