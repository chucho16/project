    private void panelNebulosa2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelNebulosa2MouseClicked
        if ((evt.getButton() == 1) && (!this.crearRuta)) {
            if (!atras) {
                SistemaPlanetario sPlanetario;
                if ((sPlanetario = nebulosaControl.reconocerSistemaPlanetario(evt.getPoint())) == null) {
                    SistemaPlanetario sistemaplanetario = new SistemaPlanetario();
                    CrearSistemaPlanetario dialog = new CrearSistemaPlanetario(new javax.swing.JFrame(), true);
                    dialog.sistemaPlanetario = sistemaplanetario;
                    dialog.setVisible(true);
                    if (!sistemaplanetario.getNombre().equals("")) {
                        sistemaplanetario.setPosX(evt.getX());
                        sistemaplanetario.setPosY(evt.getY());
                        nebulosaControl.agregarSistemaPlanetario(sistemaplanetario);
                    }
                } else {
                    if (evt.getClickCount() == 2) {
                        nebulosaControl.crearSistemaPlanetario(sPlanetario);
                        this.dispose();
                    }
                }
            } else {
                nebulosaControl.regresar();
                this.dispose();
            }
        } else {

        }
    }//GEN-LAST:event_panelNebulosa2MouseClicked

    private void panelNebulosa2MouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelNebulosa2MouseDragged
        if (evt.getButton() == 0) {
            if (!crearRuta) {
                nebulosaControl.cambiarPosicionSistemaPlanetario(evt.getPoint());
            } else {
                this.nebulosaControl.lineaTransicion[2] = evt.getX();
                this.nebulosaControl.lineaTransicion[3] = evt.getY();
                this.panelNebulosa2.setLineaTransicion(this.nebulosaControl.lineaTransicion);
            }
        }
    }//GEN-LAST:event_panelNebulosa2MouseDragged

