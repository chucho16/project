    private void panelNebulosa2MouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelNebulosa2MouseMoved
        if (evt.getX() <= 80 && (evt.getY() >= 253 && evt.getY() < 300)) {
            this.panelNebulosa2.setAtras(true);
            atras = true;
        } else {
            this.panelNebulosa2.setAtras(false);
            atras = false;
        }
    }//GEN-LAST:event_panelNebulosa2MouseMoved

